console.log(5 * 3); // da 15
console.log("5" * 3);// da 15, ya que el asterisco lo toma como numero
console.log("5" + 3);// no da 8, ya que el mas los suma como string
console.log(7 % 2);// da 1, ya que el porcentaje es el resto de la division
console.log("Gol" + "azo");// da Golazo, ya que el mas los suma como string
console.log("3" - 1);// da 2, ya que el menos lo toma como numero

